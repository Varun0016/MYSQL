use banking;
drop table fixed_deposits;

create table fixed_deposits(
fd_id int auto_increment not null,
fd_acc_num varchar (50) not null,
fd_period_year varchar(10) not null,
fd_start_date date not null,
fd_end_date date not null,
fd_interset varchar(10) not null,
fd_amount decimal(10,2) not null,
primary key(fd_id),
foreign key(fd_acc_num) references Customers(customer_account_number));

